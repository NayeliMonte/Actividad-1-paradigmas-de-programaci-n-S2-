#include <stdio.h>

int main() {
    int h;
    int d;
 
    printf("Programa para calcular cuantos km/h recorre un antílope\n"); 
    printf("Ingrese el número de horas: ");
    scanf("%d", &h); //el antilope recorre 22.5km en 2 h, si dividimos 22.5/2= 11.25, siendo 11.25km/h//
    
    d = h * 11.25;
 
    printf("La distancia recorrida por el antílope es de: %.2f km\n", d); //aqui investigue como hacer que el resultado no diera un numero tan extenso y ademas agregar la extension "km/h"
 
    return 0;
}